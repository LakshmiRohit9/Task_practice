
public class Customer_Details {
	int Cust_id;
	String Cust_name;
	String Cust_addr;
	int Cust_Mnum;
	public int getCust_id() {
		return Cust_id;
	}
	public void setCust_id(int cust_id) {
		Cust_id = cust_id;
	}
	public String getCust_name() {
		return Cust_name;
	}
	public void setCust_name(String cust_name) {
		Cust_name = cust_name;
	}
	public String getCust_addr() {
		return Cust_addr;
	}
	public void setCust_addr(String cust_addr) {
		Cust_addr = cust_addr;
	}
	public int getCust_Mnum() {
		return Cust_Mnum;
	}
	public void setCust_Mnum(int cust_Mnum) {
		Cust_Mnum = cust_Mnum;
	}
}
